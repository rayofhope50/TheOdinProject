const grade = 'D';

switch (grade) {
    case 'A':
        console.log('U got an A!');
    case 'B':
        console.log('U got an B!');
    case 'C':
        console.log('U got an C!');
    case 'D':
        console.log('U got an D!');
    case 'E':
        console.log('U got an E!');
    default:
        console.log('Not a valid grade');
}